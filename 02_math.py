print(50 + 50)  # addition
print(100 - 25)  # subtraction
print(10 * 10)  # multiplication
print(100 / 10)  # division
print(10 + 5 * 2)  # order of operations   does PEMDAS
print(20 ** 2)  # exponentiation
print(15 % 4)  # modulus operator gives the remainder
print(10 // 3)  # floor division gives the whole number without the decimal
print(round(3.14159, 2))  # rounding to 2 decimal places
print(abs(-7))  # absolute value
print(pow(2, 3))  # power function, 2 raised to the power of 3
print(max(5, 10))  # maximum value
print(min(5, 10))  # minimum value
print(sum([1, 2, 3, 4, 5]))  # sum of a list of numbers
print((3 + 5) * 2)  # using parentheses to change order of operations
 